package com.example.appmoviesseries;

public class Assistidos {

    private String assistido;
    private String avaliacao;
    private String tituloPortugues;
    private String idFilme;

    public Assistidos() {

    };

    public String getTituloPortugues() {
        return tituloPortugues;
    }

    public void setTituloPortugues(String tituloPortugues) {
        this.tituloPortugues = tituloPortugues;
    }

    public String getAssistido() {
        return assistido;
    }

    public void setAssistido(String assistido) {
        this.assistido = assistido;
    }

    public String getAvaliacao() {
        return avaliacao;
    }

    public void setAvaliacao(String avaliacao) {
        this.avaliacao = avaliacao;
    }

    public String getIdFilme() {
        return idFilme;
    }

    public void setIdFilme(String idFilme) {
        this.idFilme = idFilme;
    }

}

